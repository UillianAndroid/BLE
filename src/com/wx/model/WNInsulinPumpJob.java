package com.wx.model;

public class WNInsulinPumpJob {
	public String category;
	public String period;
	public String periodInfusion;

	public String getCategory() {
		return category;
	}

	public void setCategory(String category) {
		this.category = category;
	}

	public String getPeriod() {
		return period;
	}

	public void setPeriod(String period) {
		this.period = period;
	}

	public String getPeriodInfusion() {
		return periodInfusion;
	}

	public void setPeriodInfusion(String periodInfusion) {
		this.periodInfusion = periodInfusion;
	}

}
