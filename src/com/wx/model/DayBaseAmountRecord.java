package com.wx.model;

public class DayBaseAmountRecord {
	public String month;
	public String day;
	public String dayBaseAmount;

	public String getMonth() {
		return month;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	public String getDay() {
		return day;
	}

	public void setDay(String day) {
		this.day = day;
	}

	public String getDayBaseAmount() {
		return dayBaseAmount;
	}

	public void setDayBaseAmount(String dayBaseAmount) {
		this.dayBaseAmount = dayBaseAmount;
	}

}
